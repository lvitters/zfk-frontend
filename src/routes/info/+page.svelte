<div class="mt-4 ml-7 text-xl">
	<p>
		Das Projekt Kaffee HAG (Arbeitstitel) ist ein zeitlich unbeschränktes Kulturprojekt des Zentrums für
		Kollektivkultur e. V. (ZfK), welches aus einem Zusammenschluss des Anderswo Kollektiv e. V. und dem Conartism
		Kollektiv hervorgegangen ist. Die Mitglieder des ZfK gestalten seit Jahren einen nicht unerheblichen Teil des
		Bremer Subkulturprogramms. Im Projekt Kaffee HAG wollen wir unsere Kräfte bündeln und einen Frei- und Kulturraum
		schaffen, der die Bremer Szene nachhaltig fördert, attraktiver werden lässt und darüber hinaus die Strahlkraft
		der gesamten Stadt verstärkt.
		<br />
		<br />
		Im Rahmen dieses Projektes soll in Teilen der Räumlichkeiten des ehemaligen Kaffee HAG ein Veranstaltungsort für
		Clubbetrieb, Konzerte, Theater, Performance und sonstige Veranstaltungen sowie eine offene Werkstatt, Ateliers, Studio-
		und Proberäume entstehen. Wir möchten diese Bereiche unter einem Dach vereinen, damit im Austausch untereinander
		und während der Realisierung gemeinsamer Projekte neue Synergien entstehen.
		<br />
		<br />
		Im Fahrwasser dieses Ortes sollen Arbeits- und Vernetzungsräume sowie Lagermöglichkeiten für die Bremer Subkultur
		und ihre Initiativen geschaffen werden. Fern von konventionellen und kommerziellen Konzepten, in denen Clubs als
		reine Vergnügungsstätte dienen, möchten wir einen Ort schaffen, in dem die kreative Szene ermutigt wird, Wissen und
		Fähigkeiten zu teilen, bestehende Netzwerke zu nutzen, sie zu erweitern und neue zu schaffen. So entsteht eine Schnittstelle
		zwischen Kreativschaffenden und eine Vielfalt an Möglichkeiten für innovative Projekte. Diese stärken die überregionale
		Wahrnehmung für Bremer Künstler*innen.
	</p>
	<h1 class="mt-10 mb-4 text-2xl font-bold">Motivation</h1>
	<p>
		Im Zuge vieler verschiedener Projekte innerhalb der letzten fünf Jahre ist uns bewusst geworden, dass die lokale
		Kreativszene ein großes, aber bisher unzureichend ausgeschöpftes Potenzial besitzt. Im Vergleich mit Städten
		ähnlicher Größe hat Bremen ein weitaus geringeres subkulturelles Angebot. Für junge Menschen ist dies ein nicht
		zu unterschätzender Beweggrund, andere Städte als Lebensort vorzuziehen und Bremen zu verlassen. Diese
		Einschätzung wurde unter anderem durch die Studie „Schwarmstadt Bremen?“1 des Empirica Instituts untermauert.
		<br />
		<br />
		Bremen ist die Heimat vieler Menschen, die sich nach Orten jenseits des Mainstream sehnen. Diese Orte sind in der
		Vergangenheit zunehmend verschwunden und aktuell kaum noch aufzufinden, beziehungsweise haben sich über die letzten
		Jahre stark verändert (unter anderem Lagerhaus, Schlachthof). Diese Orte sind für das kulturelle Gefüge in der Stadt
		ebenfalls sehr wichtig, aber sprechen inzwischen ein anderes Publikum an.
		<br />
		<br />
		Die ausdrückliche Unterstützung der lokalen Politik und Presse, ein gelungenes Crowdfunding sowie der Austausch mit
		vielen Mitgliedern der Kultur-, Musik-, und Kreativszene und nicht zuletzt der hohe Andrang bei bisherigen Projekten
		zeigen, dass in Bremen ein hoher Bedarf für kulturelle Freiräume besteht und junge Menschen sich aktiv in die Stadtgestaltung
		einbringen möchten. Hier möchten wir aktiv werden und einen Raum schaffen, in dem Menschen gemeinsam kreativ arbeiten,
		sich entfalten, feiern, in den Austausch kommen und selbst Kultur mitgestalten.
	</p>
	<h1 class="mt-10 mb-4 text-2xl font-bold">Expertise</h1>
	<p>
		Unsere Mitglieder bespielen temporär und in unterschiedlichen Konstellationen die Veranstaltungsorte MS Treue,
		Tau, Pusdorf Studios, Papp, Römer, Irgendwo, Tower, P.ara und waren beteiligt an der Gestaltung verschiedener
		(temporärer) Kulturräume wie dem Unterhalb, Außerhalb, Anderswo, Irgendwo und den Pusdorf Studios. Zudem hat es
		Kooperationen mit dem 13 Grad Festival, dem Theater Bremen, der Bremer Philharmoniker, der Schwankhalle, der
		Hochschule für Künste und den Lichtern der Neustadt gegeben.
		<br />
		<br />
		Über die Sommermonate veranstalten wir Freiluftpartys und sind in die Planung und Durchführung überregionaler Großveranstaltungen,
		wie dem Fusion Festival (Müritz), Zugvögel Festival (Eifel) und Skandalös Festival (Nordfriesland) involviert. Durch
		die Umsetzung dieser Veranstaltungen und die Initiierung eines Netzwerkes für Veranstaltungskollektive stehen wir
		in regem Austausch zu zahlreichen regionalen und überregionalen subkulturellen Organisationen.
		<br />
		<br />
		Darüber hinaus sind wir im Bühnen- und Kulissenbau sowie in der Entwicklung und Produktion von Veranstaltungstechnik
		tätig. Mit diesen eigens entworfenen und produzierten Bauten haben wir uns an der Umsetzung von Projekten gemeinnütziger
		Vereine beteiligt und stellen diese nach wie vor anderen sozialen Projekten (vor allem in Bremen) zur Verfügung.
		Einige unserer Mitglieder sind ausgebildete Handwerker und dadurch in der Lage eine Werkstatt aufzubauen und professionell
		zu betreuen.
		<br />
		<br />
		Durch die Tätigkeiten der letzten Jahre sind wir fähig, Projekte dieser Größenordnung umzusetzen. Das Projekt Kaffee
		HAG hat bereits jetzt guten Anklang bei zahlreichen Kollektiven und Menschen in unserem Netzwerk gefunden, die angekündigt
		haben sich einbringen zu wollen. Durch jahrelange kollektive Zusammenarbeit haben wir zudem gelernt, in Arbeitsgruppen
		mit flachen Hierarchien und losen Strukturen effizient zu arbeiten und Entscheidungen zu treffen, ohne dabei unsere
		demokratischen Grundwerte zu vernachlässigen.
		<br />
		<br />
		Zusammenfassend bringen unsere Mitglieder jahrelange Erfahrung in den Bereichen Netzwerkarbeit, Musik, Kunst, Theater,
		Kuration, Booking, Gastronomie, Logistik, Handwerk, Veranstaltungstechnik, Veranstaltungsmanagement und Buchhaltung
		mit.
	</p>

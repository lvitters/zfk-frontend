<div class="mt-4 ml-7 text-xl">
	<h1 class="mt-10 mb-4 text-2xl font-bold">Präambel</h1>
	<p>
		Die im Selbstverständnis formulierten Ziele sind Leitfaden unseres Handelns. Es soll zusammenfassen, entlang
		welcher Werte wir Entscheidungen treffen und anhand welcher Kriterien wir uns messen wollen. Es wird angestrebt,
		die Ziele möglichst unter Berücksichtigung aller Aspekte zu erreichen. Bei entgegenstehenden Interessen muss zum
		Wohle des Vereins abgewägt werden.
	</p>

	<h1 class="mt-10 mb-4 text-2xl font-bold">Interner Umgang</h1>

	<h2 class="mt-4 mb-4 text-xl">Zusammenhalt:</h2>
	<p>
		Unser Umgang untereinander soll auf einem wertschätzenden Miteinander und auf Ehrlichkeit beruhen. Ein
		gegenseitiges Interesse und möglichst freundschaftliches Verhältnis sollen die Basis unserer Zusammenarbeit
		bilden. Wir möchten aufeinander aufpassen, uns füreinander verantwortlich fühlen, unterstützen und auf die
		Gefühle der anderen Acht geben. Alleingänge werden nicht toleriert.
	</p>
	<p></p>
	<h2 class="mt-4 mb-4 text-xl">Miteinander:</h2>
	<p>
		Jede Person soll die Chance bekommen, ihre Meinung vorzutragen und gehört zu werden. Andere Meinungen sind mit
		Offenheit und Toleranz zu begegnen. Auch bei Meinungsverschiedenheiten oder Streitgesprächen soll stets nach den
		Prinzipien der gewaltfreien Kommunikation gehandelt werden. Dazu gehören eine gewaltfreie, nicht-aggressive
		Sprache, aktives Zuhören, objektive Beschreibung des Sachverhalts, Ich-Botschaften und Empathie für das
		Gegenüber.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Entscheidungsfindung:</h2>
	<p>
		Beschlüsse werden, wie in der Satzung geregelt, nach jeweiligem Mehrheitsprinzip entschieden. Zuvor sollte
		genügend Raum für die Erörterung der Entscheidung und mögliche Kompromisslösungen eingeräumt werden.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Selbstbemächtigung durch Gemeinschaft:</h2>
	<p>
		Das Projekt soll die Möglichkeit bieten, sich auszuprobieren, zu experimentieren und Neues zu lernen. Dafür
		sollen Wissenstransparenz, -weitergabe und nachvollziehbare Dokumentation von Wissen und – daraus resultierend –
		flache Wissenshierarchien sorgen. Daraus folgt außerdem, dass Vertrauensvorschüsse gegenüber der sich
		ausprobierenden Personen gegeben werden, diese in ihrem Vorhaben unterstützt werden sowie eine generelle
		Fehlerfreundlichkeit vorherrscht und ein konstruktiver Umgang mit Fehlern etabliert wird. Das langfristige Ziel
		ist es, Fachkompetenzen in unsere Strukturen zu integrieren und innerhalb des Vereins zu fördern.
		Unterrepräsentierte Gruppen sollen in unterschiedlichen Arbeitsbereichen gefördert werden.
	</p>

	<h1 class="mt-10 mb-4 text-2xl font-bold">Nachhaltigkeit</h1>

	<h2 class="mt-4 mb-4 text-xl">Ökologisch:</h2>
	<p>
		Ökologisches Handeln und Veranstaltungsbetrieb sind nicht immer in Einklang zu bringen – dennoch wollen wir
		versuchen, ressourcenschonend zu agieren. Das bedeutet, dass wir nach Möglichkeit auf Neukäufe verzichten,
		reparieren und wieder benutzen, Müll vermeiden und energieeffizient arbeiten wollen.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Ökonomisch:</h2>
	<p>
		Ökonomisch nachhaltig zu wirtschaften bedeutet für uns, dass das Projekt nicht an Profit orientiert ist. Dies
		bedeutet, dass lediglich der Erhalt und Ausbau des Vorhabens garantiert werden soll. Ökonomische Strukturen
		werden so eingerichtet, dass der Zugang zum Kulturzentrum für möglichst viele Menschen erschwinglich bleibt.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Sozial:</h2>
	<p>
		Unser Ziel ist es, die gesellschaftlichen und sozialen Missstände – die aus konventionellen, prekären
		Anstellungsmodellen in der Kulturlandschaft resultieren – zu verändern und im Rahmen unserer Möglichkeiten zu
		verbessern. Wir streben an, möglichst viele anfallende Arbeiten zu entlohnen (siehe Punkt „Ökonomisch“). Alle,
		die entlohnt werden, sollen bedarfsorientiert, solidarisch und unter Berücksichtigung der
		Verantwortungsübernahme und Beliebtheit der Aufgabe bezahlt werden. Wir versuchen, die soziale und ökonomische
		Situation der angestellten Person zu berücksichtigen.
	</p>

	<h1 class="mt-10 mb-4 text-2xl font-bold">Ausrichtung</h1>

	<h2 class="mt-4 mb-4 text-xl">Grundwerte:</h2>
	<p>
		Wir definieren uns als demokratisch und orientieren uns an den universellen Menschenrechten. Wir sprechen uns
		gegen Homophobie, Sexismus, Rassismus, Antisemitismus, Ableismus, Faschismus und andere (intersektionale) Formen
		von Diskriminierung aus. Als gesellschaftliche Akteuer*innen mit Außenwirkung sehen wir uns in der
		Verantwortung, uns für Themen einzusetzen, die soziale Gerechtigkeit und Teilhabe stärken und uns mit
		patriarchalen und kapitalistischen Strukturen – auch innerhalb unserer Strukturen – kritisch
		auseinanderzusetzen.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Diversität:</h2>
	<p>
		Wir wollen diversitätsorientiert handeln und langfristig gesellschaftliche Differenzen abbauen. Differenzlinien
		– zu, Beispiel entlang der Kategorien Geschlecht, sexuelle Orientierung, äußeres Erscheinungsbild,
		(zugeschriebene) nationale oder ethnische Zugehörigkeit, Religion, Milieuzugehörigkeit, Behinderung – sollen
		kritisiert und transformiert werden sowie deren Verschränkungen (Intersektionen) untereinander.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Personal:</h2>
	<p>
		Bei der Anstellung externer Mitarbeitender sollen insbesondere marginalisierte Gruppen beachtet und bei
		ähnlicher Qualifizierung bevorzugt werden. Neben Personalentscheidungen, die zu einer höheren internen
		Diversität beitragen sollen, soll grundsätzlich die Teilhabe an einzelnen Projekten oder dem Gesamtvorhaben
		ermöglicht werden. Zudem streben wir die Schaffung von Aus- und Weiterbildungsmöglichkeiten.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Besucher*innen:</h2>
	<p>
		Um einer breiten Zielgruppe den Zugang zum Kulturzentrum zu ermöglichen, möchten wir sowohl physische als auch
		psychische Barrieren abbauen: Ein barrierefreier Zugang, eine Öffentlichkeitsarbeit, die ein breiteres Publikum
		erreicht, eine antidiskriminierende Türpolitik sowie Awarenesskonzepte, die ein sichereres Feiern ermöglichen,
		sollen zu Barrierefreiheit beitragen. Zuletzt erkennen wir diskriminierende Strukturen in der Gesellschaft an
		und versuchen uns mit diesen auseinanderzusetzen und ihnen entgegenzuwirken.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Programm:</h2>
	<p>
		Wir wollen ein abwechslungsreiches Angebot schaffen, das sich nicht durch Konventionen oder Genregrenzen
		einschränken lässt. Bei der Auswahl von Kunst- und Kulturschaffenden sind wir grundsätzlich offen für Neues:
		Neue Menschen, neue Ideen oder uns unbekannte Kunstformen und Musikgenres. Communities und aktivistische
		Gruppen, die sich für die Stärkung marginalisierter Menschen einsetzen, sollen die Möglichkeit bekommen,
		Veranstaltungen zu organisieren und das Programm mitzugestalten. Gesellschaftskritische Themen sollen im Rahmen
		des Veranstaltungsprogrammes bearbeitet werden und dadurch das Diversitätsbewusstsein stärken und Impulse hin zu
		einer gerechteren Gesellschaft setzen.
	</p>
	<h2 class="mt-4 mb-4 text-xl">Utopien:</h2>
	<p>
		Das Kulturzentrum soll ein Ort sein, an dem Utopien erdacht, ausprobiert und gelebt werden können. Wir wollen
		uns nicht mit dem Status quo zufriedengeben, sondern Denkanstößen, wie eine andere Gesellschaft aussehen könnte,
		einen Raum geben. Wir wollen zeigen, dass auch jenseits klassischer kapitalistischer Strukturen – geprägt von
		starken Hierarchien, Profitdruck und hohem Rationalisierungs- und Effizienzdruck – ein Kulturzentrum erfolgreich
		betrieben werden kann.
	</p>
</div>
